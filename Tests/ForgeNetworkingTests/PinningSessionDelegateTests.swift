// MARK: - Test doubles

private struct StubPolicy: PinningPolicy {
    let result: PinningResult
    func evaluate(serverTrust: SecTrust, host: String) -> PinningResult { result }
}

// MARK: - Suite

@Suite("PinningSessionDelegate")
struct PinningSessionDelegateTests {

    // MARK: Initialization

    @Test("Stores the provided policy and returns its result")
    func storesPolicy() throws {
        let stub = StubPolicy(result: .allow)
        let delegate = PinningSessionDelegate(policy: stub)
        let trust = try makeTestTrust()
        #expect(delegate.policy.evaluate(serverTrust: trust, host: "x") == .allow)
    }

    // MARK: Non-server-trust challenges → performDefaultHandling

    @Test("HTTP Basic challenge falls through to performDefaultHandling")
    func httpBasicFallsThrough() async {
        let delegate = PinningSessionDelegate(policy: StubPolicy(result: .allow))
        let disposition = await disposition(for: delegate, method: NSURLAuthenticationMethodHTTPBasic)
        #expect(disposition == .performDefaultHandling)
    }

    @Test("HTTP Digest challenge falls through to performDefaultHandling")
    func httpDigestFallsThrough() async {
        let delegate = PinningSessionDelegate(policy: StubPolicy(result: .reject))
        let disposition = await disposition(for: delegate, method: NSURLAuthenticationMethodHTTPDigest)
        #expect(disposition == .performDefaultHandling)
    }

    @Test("Client certificate challenge falls through to performDefaultHandling")
    func clientCertFallsThrough() async {
        let delegate = PinningSessionDelegate(policy: StubPolicy(result: .reject))
        let disposition = await disposition(
            for: delegate,
            method: NSURLAuthenticationMethodClientCertificate
        )
        #expect(disposition == .performDefaultHandling)
    }

    // MARK: - Helpers

    /// Builds a `URLAuthenticationChallenge` with the given auth method (no serverTrust)
    /// and returns the disposition chosen by the delegate.
    private func disposition(
        for delegate: PinningSessionDelegate,
        method: String
    ) async -> URLSession.AuthChallengeDisposition {
        let space = URLProtectionSpace(
            host: "api.example.com",
            port: 443,
            protocol: "https",
            realm: nil,
            authenticationMethod: method
        )
        let challenge = URLAuthenticationChallenge(
            protectionSpace: space,
            proposedCredential: nil,
            previousFailureCount: 0,
            failureResponse: nil,
            error: nil,
            sender: DummyChallengeSender()
        )
        let session = URLSession(configuration: .ephemeral)
        return await withCheckedContinuation { continuation in
            delegate.urlSession(session, didReceive: challenge) { disp, _ in
                continuation.resume(returning: disp)
            }
        }
    }

    private func makeTestTrust() throws -> SecTrust {
        let data = Data(testCertDERBytes)
        guard let cert = SecCertificateCreateWithData(nil, data as CFData) else {
            throw NSError(domain: "PinningSessionDelegateTests", code: 0,
                          userInfo: [NSLocalizedDescriptionKey: "Failed to create test cert"])
        }
        let sslPolicy = SecPolicyCreateSSL(false, nil)
        var trust: SecTrust?
        let status = SecTrustCreateWithCertificates([cert] as CFArray, sslPolicy, &trust)
        guard status == errSecSuccess, let t = trust else {
            throw NSError(domain: "PinningSessionDelegateTests", code: Int(status),
                          userInfo: [NSLocalizedDescriptionKey: "SecTrustCreateWithCertificates failed: \(status)"])
        }
        return t
    }
}

// MARK: - Dummy challenge sender

private final class DummyChallengeSender: NSObject, URLAuthenticationChallengeSender {
    func use(_ credential: URLCredential, for challenge: URLAuthenticationChallenge) {}
    func continueWithoutCredential(for challenge: URLAuthenticationChallenge) {}
    func cancel(_ challenge: URLAuthenticationChallenge) {}
}
